using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Ex_14_For_Loop
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int i = 1; i <= 10; i++)
            {
                Console.WriteLine("Value of i: {0}", i); //Print 1 to 10
               
            }
            Console.ReadLine();

        }
    }
}
